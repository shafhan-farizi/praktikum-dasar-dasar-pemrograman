import math

def tambah(angka1, angka2):
    hasil = angka1 + angka2
    return hasil

def kurang(angka1, angka2):
    hasil = angka1 - angka2
    return hasil

def pangkat(angka1, angka2):
    hasil = angka1 ** angka2
    return hasil

def kali(angka1, angka2):
    hasil = angka1 * angka2
    return hasil

def bagi(angka1, angka2):
    hasil = angka1 / angka2
    return hasil

def log(angka):
    hasil = math.log(angka)
    return hasil

def akar(angka):
    hasil = math.sqrt(angka)
    return hasil

def sin(angka):
    hasil = math.sin(math.radians(angka))
    return hasil

def cos(angka):
    hasil = math.cos(math.radians(angka))
    return hasil

def tan(angka):
    hasil = math.tan(math.radians(angka))
    return hasil